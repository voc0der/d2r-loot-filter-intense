const catalogFixture = require('../test/fixtures/lod-base-catalog.json');
const policy = require('../test/fixtures/lod-policy.json');

const GROUP_TITLES = {
  axes: 'Axes',
  clubsMacesHammers: 'Clubs, maces, and hammers',
  daggers: 'Daggers',
  assassinClaws: 'Assassin claws',
  throwingWeapons: 'Throwing weapons',
  genericJavelins: 'Generic javelins',
  polearms: 'Polearms',
  spears: 'Spears',
  swords: 'Swords',
  bows: 'Bows',
  crossbows: 'Crossbows',
  gloves: 'Gloves',
  boots: 'Boots',
  belts: 'Belts',
  genericHelms: 'Generic helms',
  bodyArmor: 'Body armor',
  druidPelts: 'Druid pelts',
  genericShields: 'Generic shields',
  paladinShields: 'Paladin shields',
};

function escapeCell(value) {
  return String(value).replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function collisionLabel(item) {
  const labels = [];
  if (item.unique.length > 0) {
    labels.push(`Unique: ${item.unique.join(', ')}`);
  }
  if (item.set.length > 0) {
    labels.push(`Set: ${item.set.join(', ')}`);
  }
  return labels.length > 0 ? labels.join('; ') : '—';
}

function render() {
  const catalog = catalogFixture.items;
  const hiddenKeys = Object.values(policy.hiddenGroups).flat();
  const lines = [
    '<!-- Generated by `npm run policy:generate`; edit the pinned fixtures instead. -->',
    '',
    '# Audited Hide Unpopular Bases policy',
    '',
    `This generated table is the authoritative list of all **${hiddenKeys.length}** hidden equipment base strings.`,
    `It targets the **${policy.source.ruleset}** ruleset and is pinned to d2data commit \`${policy.source.d2dataCommit}\`.`,
    '',
    'The filter is quality-blind: every listed code hides ordinary, superior, socketed, magic, rare, unique and set labels on that base. Named unique/set collisions from the pinned LoD data appear below. Staffmods, crafting uses and other runtime tradeoffs are documented in the README and policy fixture.',
    '',
    '## Headline accepted tradeoffs',
    '',
  ];

  Object.entries(policy.notableAcceptedCollisions).forEach(([code, tradeoffs]) => {
    lines.push(`- \`${code}\` ${catalog[code].name}: ${tradeoffs.join('; ')}`);
  });

  Object.entries(policy.hiddenGroups).forEach(([group, codes]) => {
    lines.push(
      '',
      `## ${GROUP_TITLES[group]} (${codes.length})`,
      '',
      '| Code | Base | Tier | Max sockets | Named unique/set collisions intentionally hidden |',
      '| --- | --- | --- | ---: | --- |',
    );
    codes.forEach((code) => {
      const item = catalog[code];
      lines.push(`| \`${code}\` | ${escapeCell(item.name)} | ${item.tier} | ${item.maxSockets} | ${escapeCell(collisionLabel(item))} |`);
    });
  });

  lines.push('', '## Explicit keeps tested by the suite', '');
  Object.entries(policy.mustStayVisible).forEach(([group, codes]) => {
    const labels = codes.map((code) => `\`${code}\` ${catalog[code].name}`);
    lines.push(`- **${group}:** ${labels.join(', ')}`);
  });
  lines.push('');
  return lines.join('\n');
}

if (require.main === module) {
  process.stdout.write(render());
}

module.exports = { render };
